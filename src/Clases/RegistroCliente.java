    public void registrarcli(){
        if (cdni.getText().isEmpty() || cnombre.getText().isEmpty() || ccelular.getText().isEmpty() || ccorreo.getText().isEmpty() || cdireccion.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "LLENE TODOS LOS CAMPOS, SINO INGRESE 'NN' ");
        }else{
            
                Cliente cli = new Cliente();

                cli.setDni(cdni.getText());
                cli.setNombre(cnombre.getText());

                cli.setCelular(ccelular.getText());
                cli.setDireccion(cdireccion.getText());
                cli.setCorreo(ccorreo.getText());
                mensaje = cbo.agregarCliente(cli);
                System.out.println(mensaje); 
                JOptionPane.showMessageDialog(null, "GUARDADO CORRECTAMENTE");
                ListaClientes lc = new ListaClientes();
                lc.setVisible(true);
                this.dispose();
            
        }
    }

